# Problem Set 6 - Question 2: Start, End, and Increment Values

#Input Phase
print("Please enter the starting value: ")
start = int(input())
print("Please enter the ending value: ")
end = int(input())
print("Please enter the increment value: ")
increment = int(input())
#Process Phase
while(start <= end):
  #Output Phase
  print(start, end = ", ")
  start = start + increment
